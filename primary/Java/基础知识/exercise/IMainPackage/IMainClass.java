package IMainPackage;

public class IMainClass {
	
	int a ;
	String b;
	boolean c;
	
	
	
	
	
	public IMainClass() {
		super();
	}
	public IMainClass(int a, String b, boolean c) {
		super();
		this.a = a;
		this.b = b;
		this.c = c;
	}
	public int getA() {
		return a;
	}
	public void setA(int a) {
		this.a = a;
	}
	public String getB() {
		return b;
	}
	public void setB(String b) {
		this.b = b;
	}
	public boolean isC() {
		return c;
	}
	public void setC(boolean c) {
		this.c = c;
	}

	
}
